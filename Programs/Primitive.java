class Primitive
{
	public static void main(String args[])
	{
	boolean b = false;
	char ch = 'S';
	byte by = 127;
	short sh = 555;
	int i = 99999;
	float f = 45.45F;
	long l = 9999999999L;
	double d = 56.7890;
	System.out.println(b);
	System.out.println(ch);
	System.out.println(by);
	System.out.println(sh);
	System.out.println(i);
	System.out.println(f);
	System.out.println(l);
	System.out.println(d);
	} 

}